

CREATE TABLE EMPLOYEE (
    id BIGINT AUTO_INCREMENT PRIMAARY KEY,
    name VARCHAR(255) NOT NULL,
    base_salary DECIMAL (10,2),
    employee_type VARCHAR(50) NOT NULL,
    bonus DECIMAL (10,2,
    project_count INT DEFAULT 0,
    project_bonus DOUBLE DEFAULT 0,
    age INT NOT NULL,
    INDEX (employee_type)

)
